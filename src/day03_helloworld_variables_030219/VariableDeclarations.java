package day03_helloworld_variables_030219;

public class VariableDeclarations {
	public static void main(String[] args) {
		// Declare single variable
		int apples;
		// Declare multiple variables in single statement
		int bananas, oranges, mango;

		// Declare variable and assign value in single statement
		int kiwi = 14;

		System.out.println(kiwi);

		// Declare and assign value to multiple variables in in a single statement

		int watermelon = 11, grapes = 100, cherries = 12, apricots;

	}
}
